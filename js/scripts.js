function romanNumeralConverter(number) {
  // var numeralOne = I;
  if (number === 1) {
    return "I";
    console.log("I");
  } else if (number === 5) {
    return "V";
    console.log("V");
  } else if (number === 10) {
    return "X";
    console.log("X");
  } else if (number === 50) {
    return "L";
    console.log("L");
  } else if (number === 100) {
    return "C";
    console.log("C");
  } else if (number === 500) {
    return "D";
    console.log("D");
  } else if (number === 1000) {
    return "M";
    console.log("M");
  }
}
